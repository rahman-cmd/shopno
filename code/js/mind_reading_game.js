var num = Math.floor(Math.random() * 20);
alert("আপন মনে একটি সংখ্যা কল্পনা করুন");
alert("এবার ঐ একই সংখ্যাটি দ্বিতীয়বার আবারও মনে মনে পূর্বের কল্পিত সংখ্যাটির সহিত যোগ করুন");
alert("এখন " + num + " যুক্ত করুন এবং সর্বমোট হিসাব করুন");
num = num/2;
alert("এখন সর্বমোট হিসাব করা সংখ্যাটি অর্ধেক করুন");
alert("এবং পূর্বের কল্পিত সংখ্যাটি এই সর্বমোট হিসাবকৃত সংখ্যাটি হতে বিয়োগ করে ফেলুন");
document.write("<h1>আপনার নিকট অবশিষ্ট সংখ্যাটি হলো " + num + " </h1>");